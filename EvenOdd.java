class EvenOdd 
{
    public static void main(String[] args) 
  {

        int num1 = 26;
        int num2 = 35;

        if (num1 % 2 == 0)
            System.out.println(num1+ " is an even number");

        else
            System.out.println(num1 + " is an odd number");


        if (num2 % 2 == 0)
            System.out.println(num2 + " is an even number");

        else
            System.out.println(num2 + " is an odd number");
    }
}